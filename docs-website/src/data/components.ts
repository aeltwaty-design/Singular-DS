// =============================================================================
// UTILITY FUNCTIONS
// =============================================================================

export function getCategoryBySlug(slug: string): CategoryMeta | undefined {
  return componentCategories.find((cat) => cat.slug === slug);
}

export function getComponentBySlug(categorySlug: string, componentSlug: string): ComponentMeta | undefined {
  const category = getCategoryBySlug(categorySlug);
  return category?.components.find((comp) => comp.slug === componentSlug);
}

export function getAllComponents(): ComponentMeta[] {
  return componentCategories.flatMap((cat) => cat.components);
}

export function getComponentCount(): number {
  return getAllComponents().length;
}

export function getComponentCountByCategory(categorySlug: string): number {
  const category = getCategoryBySlug(categorySlug);
  return category?.components.length ?? 0;
}

