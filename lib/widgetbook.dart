/// Typography preview widget
class _TypographyPreview extends StatelessWidget {
  const _TypographyPreview({required this.category});

  final String category;

  @override
  Widget build(BuildContext context) {
    final colors = context.colors;
    final typography = context.typography;

    final Map<String, TextStyle> styles = {};

    if (category == 'Display' || category == 'All') {
      styles.addAll({
        'displayLarge (57px)': typography.displayLarge,
        'displayMedium (45px)': typography.displayMedium,
        'displaySmall (36px)': typography.displaySmall,
      });
    }

    if (category == 'Headline' || category == 'All') {
      styles.addAll({
        'headlineLarge (32px)': typography.headlineLarge,
        'headlineMedium (28px)': typography.headlineMedium,
        'headlineSmall (24px)': typography.headlineSmall,
      });
    }

    if (category == 'Title' || category == 'All') {
      styles.addAll({
        'titleLarge (22px)': typography.titleLarge,
        'titleMedium (16px)': typography.titleMedium,
        'titleSmall (14px)': typography.titleSmall,
      });
    }

    if (category == 'Label' || category == 'All') {
      styles.addAll({
        'labelLarge (14px)': typography.labelLarge,
        'labelMedium (12px)': typography.labelMedium,
        'labelSmall (11px)': typography.labelSmall,
      });
    }

    if (category == 'Body' || category == 'All') {
      styles.addAll({
        'bodyLarge (16px)': typography.bodyLarge,
        'bodyMedium (14px)': typography.bodyMedium,
        'bodySmall (12px)': typography.bodySmall,
      });
    }

    return Scaffold(
      backgroundColor: colors.bgPrimary,
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(16),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Text(
              '$category Styles',
              style: typography.headlineMedium.copyWith(color: colors.textPrimary),
            ),
            const SizedBox(height: 8),
            Text(
              'Font Family: ${typography.fontFamily}',
              style: typography.bodySmall.copyWith(color: colors.textSecondary),
            ),
            const SizedBox(height: 24),
            ...styles.entries.map((entry) {
              return Padding(
                padding: const EdgeInsets.only(bottom: 16),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Text(
                      entry.key,
                      style: typography.labelSmall.copyWith(
                        color: colors.textSecondary,
                      ),
                    ),
                    const SizedBox(height: 4),
                    Text(
                      'The quick brown fox jumps over the lazy dog',
                      style: entry.value.copyWith(color: colors.textPrimary),
                    ),
                    const Divider(),
                  ],
                ),
              );
            }),
          ],
        ),
      ),
    );
  }
}

