/// Color swatch preview widget
class _ColorSwatchPreview extends StatelessWidget {
  const _ColorSwatchPreview({
    required this.name,
    required this.swatch,
  });

  final String name;
  final Map<int, Color> swatch;

  @override
  Widget build(BuildContext context) {
    final colors = context.colors;
    final typography = context.typography;

    return Scaffold(
      backgroundColor: colors.bgPrimary,
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(16),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Text(
              name,
              style: typography.headlineMedium.copyWith(color: colors.textPrimary),
            ),
            const SizedBox(height: 16),
            ...swatch.entries.map((entry) {
              final step = entry.key;
              final color = entry.value;
              final hexCode = '#${color.value.toRadixString(16).substring(2).toUpperCase()}';
              final isLight = color.computeLuminance() > 0.5;

              return Container(
                height: 60,
                margin: const EdgeInsets.only(bottom: 4),
                decoration: BoxDecoration(
                  color: color,
                  borderRadius: BorderRadius.circular(8),
                ),
                padding: const EdgeInsets.symmetric(horizontal: 16),
                child: Row(
                  mainAxisAlignment: MainAxisAlignment.spaceBetween,
                  children: [
                    Text(
                      step.toString(),
                      style: TextStyle(
                        color: isLight ? Colors.black : Colors.white,
                        fontWeight: FontWeight.bold,
                        fontSize: 16,
                      ),
                    ),
                    Text(
                      hexCode,
                      style: TextStyle(
                        color: isLight ? Colors.black54 : Colors.white70,
                        fontSize: 14,
                        fontFamily: 'monospace',
                      ),
                    ),
                  ],
                ),
              );
            }),
          ],
        ),
      ),
    );
  }
}

