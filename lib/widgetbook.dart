class _BrandAddonWrapper extends StatefulWidget {
  const _BrandAddonWrapper({required this.child});

  final Widget child;

  @override
  State<_BrandAddonWrapper> createState() => _BrandAddonWrapperState();
}

class _BrandAddonWrapperState extends State<_BrandAddonWrapper> {
  SingularBrand _selectedBrand = SingularBrand.walaPlus;

  @override
  Widget build(BuildContext context) {
    return Column(
      children: [
        // Brand selector
        Container(
          padding: const EdgeInsets.all(8),
          color: Colors.grey.shade200,
          child: Row(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              const Text('Brand: ', style: TextStyle(fontWeight: FontWeight.bold)),
              const SizedBox(width: 8),
              SegmentedButton<SingularBrand>(
                segments: SingularBrand.values.map((brand) {
                  return ButtonSegment<SingularBrand>(
                    value: brand,
                    label: Text(brand.displayName),
                  );
                }).toList(),
                selected: {_selectedBrand},
                onSelectionChanged: (selection) {
                  setState(() {
                    _selectedBrand = selection.first;
                  });
                },
              ),
            ],
          ),
        ),
        // Content with brand context
        Expanded(
          child: _BrandContext(
            brand: _selectedBrand,
            child: widget.child,
          ),
        ),
      ],
    );
  }
}

class _BrandContext extends InheritedWidget {
  const _BrandContext({
    required this.brand,
    required super.child,
  });

  final SingularBrand brand;

  static SingularBrand of(BuildContext context) {
    final widget = context.dependOnInheritedWidgetOfExactType<_BrandContext>();
    return widget?.brand ?? SingularBrand.walaPlus;
  }

  @override
  bool updateShouldNotify(_BrandContext oldWidget) => brand != oldWidget.brand;
}

